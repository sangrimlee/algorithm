import { rotateTheBox } from './leetcode-1861';

describe('2024-11-23: LeetCode 1861', () => {
  test('Example 1', () => {
    expect(rotateTheBox([['#', '.', '#']])).toEqual([['.'], ['#'], ['#']]);
  });

  test('Example 2', () => {
    expect(
      rotateTheBox([
        ['#', '.', '*', '.'],
        ['#', '#', '*', '.'],
      ]),
    ).toEqual([
      ['#', '.'],
      ['#', '#'],
      ['*', '*'],
      ['.', '.'],
    ]);
  });

  test('Example 3', () => {
    expect(
      rotateTheBox([
        ['#', '#', '*', '.', '*', '.'],
        ['#', '#', '#', '*', '.', '.'],
        ['#', '#', '#', '.', '#', '.'],
      ]),
    ).toEqual([
      ['.', '#', '#'],
      ['.', '#', '#'],
      ['#', '#', '*'],
      ['#', '*', '.'],
      ['#', '.', '*'],
      ['#', '.', '.'],
    ]);
  });
});
